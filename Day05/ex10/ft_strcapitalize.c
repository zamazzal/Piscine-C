char	*ft_strcapitalize(char *str)
{
	int x;

	x = 0;
	while (str[x] != '\0')
	{
		if (((str[x - 1] >= '0') && (str[x - 1] <= '9')) || ((str[x - 1] >= 'a')
		&& (str[x - 1] <= 'z')) || ((str[x - 1] >= 'A') && (str[x - 1] <= 'Z')))
		{
			if ((str[x] >= 'A') && (str[x] <= 'Z'))
				str[x] = str[x] + 32;
		}
		else
		{
			if ((str[x] >= 'a') && (str[x] <= 'z'))
				str[x] = str[x] - 32;
		}
		x++;
	}
	return (str);
}
