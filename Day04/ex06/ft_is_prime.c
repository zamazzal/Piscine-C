int		ft_is_prime(int nb)
{
	int testeur;

	testeur = 2;
	if (nb < 2)
	{
		return (0);
	}
	while (testeur <= nb / 2)
	{
		if (nb % testeur == 0)
		{
			return (0);
		}
		testeur++;
	}
	return (1);
}
